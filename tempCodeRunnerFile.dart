void main() {
      // Unicode for heart symbol (♥)
      String heart = '\u2665'; 
    
      // Unicode for smiley face (☺)
      String smiley = '\u263A';
    
      // Unicode for star symbol (★)
      String star = '\u2605';
    
      // Unicode for musical note (♫)
      String musicNote = '\u266B';
    
      // Printing all Unicode symbols
      print(heart);       // Output: ♥
      print(smiley);      // Output: ☺
      print(star);        // Output: ★
      print(musicNote);   // Output: ♫

}